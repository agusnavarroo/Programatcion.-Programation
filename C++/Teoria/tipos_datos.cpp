// Tipos de datos en C++

#include <iostream>

using namespace std;

int main(){

    int entero = 10; // Dato tipo entero
    float flotante = 10.43; // Dato tipo flotante, numero con coma
    double mayor = 10.5436; // Dato tipo doble, es numero pero despues de la coma el numero es mas grande, soporta mas cantitdad
    char caracter = '1'; // Dato tipo caracter, entre comillas simples

    cout<<entero;
    cout<<flotante;
    cout<<mayor;
    cout<<caracter;

    return 0;

}